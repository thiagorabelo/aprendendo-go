func AtualizarPublicacao(w http.ResponseWriter, r *http.Request) {
	usuarioId, err := autenticacao.ExtrairUsuarioId(r)
	if err != nil {
		respostas.Erro(w, http.StatusUnauthorized, err)
		return
	}

	parametros := mux.Vars(r)
	publicacaoId, err := strconv.ParseUint(parametros["publicacaoId"], 10, 64)
	if err != nil {
		respostas.Erro(w, http.StatusBadRequest, err)
		return
	}

	db, err := banco.Conectar()
	if err != nil {
		respostas.Erro(w, http.StatusInternalServerError, err)
		return
	}
	defer db.Close()

	repositorio := repositorios.NovoRepositorioDePublicacoes(db)
	publicacaoPersistida, err := repositorio.BuscarPorId(publicacaoId)
	if err != nil {
		respostas.Erro(w, http.StatusInternalServerError, err)
		return
	}

	if publicacaoPersistida.AutorId != usuarioId {
		respostas.Erro(w, http.StatusForbidden, errors.New("não é permitido atualizar uma publicação de terceiro"))
		return
	}

	body, err := io.ReadAll(r.Body)
	if err != nil {
		respostas.Erro(w, http.StatusUnprocessableEntity, err)
		return
	}

	var publicacao modelos.Publicacao
	if err := json.Unmarshal(body, &publicacao); err != nil {
		respostas.Erro(w, http.StatusBadRequest, err)
		return
	}

	if err := publicacao.Preparar(); err != nil {
		respostas.Erro(w, http.StatusBadRequest, err)
		return
	}

	if err := repositorio.Atualizar(publicacaoId, publicacao); err != nil {
		respostas.Erro(w, http.StatusInternalServerError, err)
		return
	}

	respostas.JSON(w, http.StatusNoContent, nil)
}

